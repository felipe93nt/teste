package br.com.bradesco.web.ricc.model.cadastro.questionarioagronegocio;

public class AreaCorteUsinasVO {

	private String ano = "";
	private String idadeMediaCanavial = "";
	private String porcentagemColheitaMecanizada = "";
	private String raioMedioCanavial = "";

	public String getAno() {
		return ano;
	}

	public void setAno(String ano) {
		this.ano = ano;
	}

	public String getIdadeMediaCanavial() {
		return idadeMediaCanavial;
	}

	public void setIdadeMediaCanavial(String idadeMediaCanavial) {
		this.idadeMediaCanavial = idadeMediaCanavial;
	}

	public String getPorcentagemColheitaMecanizada() {
		return porcentagemColheitaMecanizada;
	}

	public void setPorcentagemColheitaMecanizada(
			String porcentagemColheitaMecanizada) {
		this.porcentagemColheitaMecanizada = porcentagemColheitaMecanizada;
	}

	public String getRaioMedioCanavial() {
		return raioMedioCanavial;
	}

	public void setRaioMedioCanavial(String raioMedioCanavial) {
		this.raioMedioCanavial = raioMedioCanavial;
	}

}
