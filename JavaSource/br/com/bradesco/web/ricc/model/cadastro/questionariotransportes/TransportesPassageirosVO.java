package br.com.bradesco.web.ricc.model.cadastro.questionariotransportes;

public class TransportesPassageirosVO {

	private String ano = "";

	private String prcMunicipal = "";
	private String prcIntermunicipal = "";
	private String prcInterestadual = "";
	private String prcInternacional = "";
	private String prcFretamento = "";

	public String getAno() {
		return ano;
	}

	public void setAno(String ano) {
		this.ano = ano;
	}

	public String getPrcMunicipal() {
		return prcMunicipal;
	}

	public void setPrcMunicipal(String prcMunicipal) {
		this.prcMunicipal = prcMunicipal;
	}

	public String getPrcIntermunicipal() {
		return prcIntermunicipal;
	}

	public void setPrcIntermunicipal(String prcIntermunicipal) {
		this.prcIntermunicipal = prcIntermunicipal;
	}

	public String getPrcInterestadual() {
		return prcInterestadual;
	}

	public void setPrcInterestadual(String prcInterestadual) {
		this.prcInterestadual = prcInterestadual;
	}

	public String getPrcInternacional() {
		return prcInternacional;
	}

	public void setPrcInternacional(String prcInternacional) {
		this.prcInternacional = prcInternacional;
	}

	public String getPrcFretamento() {
		return prcFretamento;
	}

	public void setPrcFretamento(String prcFretamento) {
		this.prcFretamento = prcFretamento;
	}

}
